export class Calculator{
    
    add(num1, num2){
        return num1 + num2
    }
    multiply(num1, num2){
        const result = num1 * num2
        return result
    }
    subtract(num1, num2){
        const difference = num1 -num2
        return difference
    }
    divide(num1, num2){
        const quotient = num1/num2
        return quotient
    }
    
}